# Class: a blueprint for an instance
# Instance: a constructed object of the class
# Object: An object is a unit of data (having one or more attributes), of a particular class or type, with associated
# functionality (methods).
# Type: indicates the class the instance belongs to
# Attribute: any object value: object.attribute
# Method: a "callable attribute" defined in the class
# Bound Methods: instance methods are called bound methods because the instance is passed automatically to the method
