# casefold()
'''
 |  casefold(...)
 |      S.casefold() -> str
 |      
 |      Return a version of S suitable for caseless comparisons.
'''
string = "Mohamed Alanasary (Cod3R30)"
print("The string is '%s'" % string)
print("The version of the string suitable for caseless comparisons is '%s'" % string.casefold())
