# lower()
'''
 |  lower(...)
 |      S.lower() -> str
 |      
 |      Return a copy of the string S converted to lowercase.
'''
string = "This iS a stirng"
print("The string '%s' converted to lower case is '%s'" % (string, string.lower()))
string = "12@_.."
print("The string '%s' converted to lower case is '%s'" % (string, string.lower()))
string = "this is a string"
print("The string '%s' converted to lower case is '%s'" % (string, string.lower()))
string = "THIS IS A STRING"
print("The string '%s' converted to lower case is '%s'" % (string, string.lower()))

