# upper()
'''
 |  upper(...)
 |      S.upper() -> str
 |      
 |      Return a copy of S converted to uppercase.
'''
string = "This iS a stirng"
print("The string '%s' converted to upper case is '%s'" % (string, string.upper()))
string = "12@_.."
print("The string '%s' converted to upper case is '%s'" % (string, string.upper()))
string = "THIS IS A STRING"
print("The string '%s' converted to upper case is '%s'" % (string, string.upper()))
string = "this is a string"
print("The string '%s' converted to upper case is '%s'" % (string, string.upper()))

