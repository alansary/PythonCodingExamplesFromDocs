# swapcase()
'''
 |  swapcase(...)
 |      S.swapcase() -> str
 |      
 |      Return a copy of S with uppercase characters converted to lowercase
 |      and vice versa.
'''
string = ""
print("Swapcase of the empty string '%s', we have: '%s'" % (string, string.swapcase()))
string = "This is A sTrinG"
print("Swapcase of the string '%s', we have: '%s'" % (string, string.swapcase()))
string = "x888x!!££$ThIS iS @ sTrInGo777o$££!!"
print("Swapcase of the string '%s', we have: '%s'" % (string, string.swapcase()))

