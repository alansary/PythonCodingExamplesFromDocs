# capitalize()
'''
 |  capitalize(...)
 |      S.capitalize() -> str
 |      
 |      Return a capitalized version of S, i.e. make the first character
 |      have upper case and the rest lower case.
'''
string = "This is a string"
print(string, string.capitalize())
string.capitalize()
print(string)
